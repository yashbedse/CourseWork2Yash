#!/bin/bash

# ----------------------------------------------------------------------
# Move to project directory
# ----------------------------------------------------------------------
cd /app

# ----------------------------------------------------------------------
# Check is node_modules exist
# ----------------------------------------------------------------------
if [[ ! -d "/app/node_modules" ]];
then
echo "––––––––––––––––––––––––––––––––––––––––"
echo "Installing package.json dependencies"
echo "––––––––––––––––––––––––––––––––––––––––"
npm install
fi

echo "––––––––––––––––––––––––––––––––––––––––"
echo "Starting npm watch ..."
echo "––––––––––––––––––––––––––––––––––––––––"
npm run watch
